const cardColors = ["red", "red", "green", "green", "blue", "blue", "brown", "brown", "yellow", "yellow", "gray", "gray", "cadetblue", "cadetblue", "violet", "violet", "lightgreen", "lightgreen"];

let cards = document.querySelectorAll('div');
cards = [...cards];
//liczenie czasu gry 
const startTime = new Date().getTime();

let activeCard ='';
const activeCards = [];

const gamePairs = cards.length/2;
let gameResault = 0;




//część skryptu odpowiedzialna za wybranie kart, przypisanie nieaktywnych kart oraz okran końcowy
const clickCard = function(){
    activeCard = this;

    if(activeCard == activeCards[0]){
        return;
    }
    //dodawanie classy hidden w celu odkrycia karty
    activeCard.classList.remove('hidden');
    //sprawdzenie czy nie klikłeś na ta samą kartę 2 razy
    if(activeCards.length === 0){
        activeCards[0] = activeCard;
        return;
    }
    
    else{
        cards.forEach(card =>{
            card.removeEventListener('click', clickCard)    
        })
        activeCards[1]=activeCard;
        //jeżeli znajdziesz dwie takie same karty to skrypt doda im klasę off oraz licznik punktów wzrośnie o 1
        setTimeout(function(){if(activeCards[0].className === activeCards[1].className){
            console.log('wygrana')
            activeCards.forEach(card => card.classList.add('off'))
            //liczenie punktów
            gameResault++;
            //część skryptu odpowiedzialna za ekran końcowy
            //jeżeli punkty będą takie same co ilość par gra zakończy się
            if(gameResault == gamePairs){
                const endTime = new Date().getTime();
                const gameTime = (endTime - startTime)/1000;
                //menu po wygranej grze
                alert(`WYGRANA! CZAS JAKI POTRZEBOWAŁEŚ TO: ${gameTime} sec`)
                if(confirm(`CZY CHCESZ GRAĆ OD NOWA?`)){
                    location.reload();
                }
                else{
                    location.href='gamemainpage.html';
                };
            }
        }
        //część skryptu która aktywóje się po niezgadnięciu połżenia dwóch takich samych kart
        else{
            console.log('przegrana')
            activeCards.forEach(card => card.classList.add('hidden'))
        }
        activeCard ='';
        activeCards.length = 0;
        cards.forEach(card => card.addEventListener('click', clickCard))
    
    },1000)
         
    }
};
//skrypt który jest odpowiedzialny za przypisanie na początku gry do divów odpowiednich kart które są zdefiniowane u samej góry skryptu oraz w pliku css
